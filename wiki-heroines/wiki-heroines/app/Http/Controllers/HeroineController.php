     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nom'=>'required',
            'genre'=>'required',
            'pouvoir'=>'required',
            'année'=>'required'
        ]);
        Heroine::create([
            'nom' => $request->nom,
            'description' => $request->description,
            'pouvoir' => $request->pouvoir,
            'année' => $request->année,
         ]);
        return redirect()->route('heroines.index');

    }

